class Solution {
public:
    int minTimeToType(string word) {
        int i = 'a', ans = word.size();
        for (char x: word)
        {
            if (x < i) ans += min((int)x + 26 - i, (int)i - x);
            else if (x > i) ans += min((int)x - i, (int)i + 26 - x);
            i = x;
        }
        return ans;
    }
};

